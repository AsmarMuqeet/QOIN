class knapsack(abstract_interface):

    def __init__(self, **kwargs):
        self.key_aurguments = kwargs

    def get_result(self, backend, input_data, number_of_runs=1024, seed=1997):

        values = input_data[0]
        weights = input_data[1]
        max_weight = input_data[2]

        quantum_instance = QuantumInstance(backend, shots=number_of_runs, seed_transpiler=seed, seed_simulator=seed)

        count_results = []
        EXE = quantum_instance.execute
        def new_execute(circuits, had_transpiled=False):
            result = EXE(circuits, had_transpiled)
            count_results.append(result)
            return result
        quantum_instance.execute = new_execute

        prob = Knapsack(values=values, weights=weights, max_weight=max_weight)
        qp = prob.to_quadratic_program()

        meo = MinimumEigenOptimizer(min_eigen_solver=QAOA(reps=1, quantum_instance=quantum_instance))
        result = meo.solve(qp)

        output = prob.interpret(result)

        data = {"probability": [], "output": output}
        for k, v in count_results[-1].get_counts().items():
            bin_str = k
            dec_str = convert_to_int(k)
            str_str = convert_to_str(k)

            try:
                odds = v / sum([value for key, value in count_results[-1].get_counts().items() if key != k])
                prob = v / sum([value for key, value in count_results[-1].get_counts().items()])
            except:
                odds = 1
            data["probability"].append({"bin": bin_str, "dec": dec_str, "ascii": str_str, "count": v, "odds": odds,"prob":prob})

        return data

    def get_collective_result(self, backend, input_data, number_of_runs=1024, seed=1997, iterations=2):
        return [self.get_result(backend, input_data, number_of_runs, seed) for x in range(iterations)]

